package com.example.test.Catego.Cat_dispo;

public class CategoriaD {
    String ecosis;
    String imagen;

    public CategoriaD() {
    }


    public CategoriaD(String ecosis, String imagen) {
        this.ecosis = ecosis;
        this.imagen = imagen;
    }

    public String getEcosis() {
        return ecosis;
    }

    public void setEcosis(String ecosis) {
        this.ecosis = ecosis;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
}
